// winding typecastinh
// smaller to lower datatypes

class typecasting
{
	public static void main(String args[])
	{
		int a = 12;
		double b = a;
		System.out.println(a);
		System.out.println(b);
	


// narrowing typecasting
// lower to smaller datatypes
	
		double d = 4.5d;
		int c = (int)d;
		System.out.println(d);
		System.out.println(c);
	}
}
		

		